// Function to check if a string is a Tamil letter
export const isTamilLetter = (str) => {
  return tamilLetters.includes(str);
};

// Function to check if a string is a Tamil word (contains multiple letters)
export const isTamilWord = (str) => {
  // Split the string into individual characters
  const chars = str.split('');
  // Check if all characters are Tamil letters
  return chars.every(char => tamilLetters.includes(char)) && chars.length > 1;
};

