// Function to convert English key to Tamil character
export const englishToTamil = (englishKey) => {
  return tamilMapping[englishKey] || englishKey;
};

// Function to convert Tamil character to English key
export const tamilToEnglish = (tamilChar) => {
  const entry = Object.entries(tamilMapping).find(([_, value]) => value === tamilChar);
  return entry ? entry[0] : tamilChar;
};

// Function to convert performance data from English keys to Tamil
export const convertPerformanceDataToTamil = (data) => {
  const convertedData = {};
  Object.entries(data).forEach(([key, value]) => {
    const tamilChar = englishToTamil(key);
    convertedData[tamilChar] = value;
  });
  return convertedData;
};

// Function to convert performance data from Tamil to English keys
export const convertPerformanceDataToEnglish = (data) => {
  const convertedData = {};
  Object.entries(data).forEach(([key, value]) => {
    const englishKey = tamilToEnglish(key);
    convertedData[englishKey] = value;
  });
  return convertedData;
}; 